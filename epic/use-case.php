		<h2>Walter Lawrence - Pet Owner</h2>
		<h3>Use Case</h3>
		<p>Walter lost his dog after the leash slipped out of his hands when his heeler mix lunged after a pigeon.</p>
		<h3>User Story:</h3>
		<p>Walter needs a website where he can connect with people who have located missing pets.</p>
		<h3>Interaction Flow:</h3>
		<ol>
			<li>After searching his neighborhood for hours to find his lost dog, Walt navigates to www.lostpaws.com on his smart phone, at the recommendation of a coworker.</li>
			<li>Walt is greeted to the LostPaws homepage with three options, Search Lost and Found Pets, Report a Missing Pet, and User Login.</li>
			<li>Walt taps the Search Lost and Found Pets button.</li>
			<li>Walt is then presented with a list of lost pets in Albuquerque.</li>
			<li>Walt clicks the filter menu  and filters results to only view lost dogs reported today.</li>
			<li>Unfortunately Walt did not find his dog. So he clicks the  Report a Missing Pet button in the navigatation at the bottom of the page.</li>
			<li>Walt sets up a user account by signing in with his google id, clicks the "I have lost my pet" radio button, then provides LostPaws with the following information about his lost dog: species(cat or dog),name, description, distinguishing features, date lost, location last seen, and .</li>
			<li>After submitting the lost dog posting, Walt is given the option to print out an automatically generated lost dog poster. Walt prints 80 copies and plasters every telephone pole in his neighborhood.</li>
			<li>Three weeks later Walt receives a phone call from a young college student named Margo. Margo found walt's dog in the Bosque. Walter arranges to meet with Margo the same day at the local police station to be reunited with his beloved heeler mix.</li>
			<li>Walt updates his lost pet listing to "found."</li>
		</ol>



		<h2>Kristi Wilkins - Pet Finder</h2>
		<h3>Use Case&#58;</h3>
		<p>Kristi found a dog while on her commute home from work. She is looking for a way to let other people know so that she
			can return the dog to its rightful owner.</p>
		<h3>User Story&#58;</h3>
		<p>Kristi needs a website that allows her to make a post with relevant information on the dog, so she can
		get in contact with the owner and return the dog.</p>
		<h3>Interaction Flow&#58;</h3>
		<ul>
			<li>Kristi arrives at the homepage.</li>
			<li>The homepage displays buttons showing lost a pet, found a pet, search, and create account/login.</li>
			<li>Kristi clicks on search and filters through recent lost dogs.</li>
			<li>Kristi looks through the search results but doesn't find a post for the dog she found.</li>
			<li>Kristi decides to make an account so that she can make a post about the dog.</li>
			<li>She clicks on create account and a small popup asking her to make an account through Facebook, Google, or e-mail.</li>
			<li>Kristi decides to log in through Facebook for easy communication.</li>
			<li>Kristi is then brought to a new webpage.</li>
			<li>The webpage displays a space to upload a picture as well as multiple lines to fill out name, description, etc.</li>
			<li>She fills out the information and scrolls down to a map with the ability to pin the area where the dog was found.</li>
			<li>She finalizes the post and waits to see if the owner gets in contact with her.</li>
			<li>A few days later Kristi receives a message via Facebook from the owner.</li>
			<li>They schedule a time and place to meet so the dog can be returned to the owner.</li>
			<li>The owner is reunited with his dog and Kristi changes the status of her post to returned!</li>
		</ul>
